    pub fn living_maze() -> Self {
        let mut cells = Vec::new();
        for x in -50..=50 {
            for y in -50..=50 {
                if x % 4 == 0 || y % 4 == 0 {
                    continue; 
                }
                if (x + y) % 7 != 0 {
                    cells.push((x, y)); 
                }
            }
        }
        for i in 0..20 {
            let x = (i * 7) % 100 - 50;
            let y = (i * 11) % 100 - 50;
            cells.push((x, y));
        }

        Self::new(
            "Живой лабиринт",
            "Лабиринт распадающийся на много движущихся частей. ",
            cells,
        )
    }
    pub fn quantum_noise() -> Self {
        let mut cells = Vec::new();
        let size = 60;
        for x in -size..=size {
            for y in -size..=size {
                let wave1 = ((x as f32 * 0.1).sin() * 10.0) as i32;
                let wave2 = ((y as f32 * 0.15).cos() * 8.0) as i32;
                let interference = (wave1 + wave2) % 3;
                if interference == 0 {
                    cells.push((x, y));
                }
            }
        }

        Self::new(
            "Квантовый шум",
            "Создаёт множество разных движений, спустя время стабилизируется.",
            cells,
        )
    }
