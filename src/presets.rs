use crate::core::{Coord, World};

#[derive(Debug, Clone)]
pub struct Preset {
    pub name: String,
    pub description: String,
    pub cells: Vec<(i32, i32)>,
}

impl Preset {
    pub fn new(name: impl Into<String>, description: impl Into<String>, cells: Vec<(i32, i32)>) -> Self {
        Self {
            name: name.into(),
            description: description.into(),
            cells,
        }
    }

    pub fn to_world(&self) -> World {
        let mut world = World::new();
        for &(x, y) in &self.cells {
            world.set_cell(Coord::new(x, y), true);
        }
        world
    }

    pub fn all() -> Vec<Preset> {
        vec![
            Self::empty(),
            Self::glider(),
            Self::blinker(),
            Self::block(),
            Self::gates(),
            Self::pulsar(),
            Self::focus(),
            Self::ship(),
            Self::bagofmth(),
            Self::flower(),
            Self::smg(),
            Self::zigzag(),
            Self::stabil(),
            Self::butterfly(),
            Self::traffic_light(),
            Self::cross(),
            Self::heart(),
            Self::smiley(),
            Self::arrow_up(),
            Self::glider_gun(),
            Self::all_in(),
            Self::living_maze(),
            Self::quantum_noise(),
        ]
    }
}

